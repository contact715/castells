"use client"

import React, { useState, useEffect } from "react"
import { motion, AnimatePresence } from "framer-motion"
import Image from "next/image"
import { cn } from "@/lib/utils"

interface Feature {
    step: string
    title?: string
    content: string
    image: string
}

interface FeatureStepsProps {
    features: Feature[]
    className?: string
    title?: string
    autoPlayInterval?: number
    imageHeight?: string
}

export function FeatureSteps({
    features,
    className,
    title = "How to get Started",
    autoPlayInterval = 3000,
    imageHeight = "h-[400px]",
}: FeatureStepsProps) {
    const [currentFeature, setCurrentFeature] = useState(0)
    const [progress, setProgress] = useState(0)

    useEffect(() => {
        const timer = setInterval(() => {
            if (progress < 100) {
                setProgress((prev) => prev + 100 / (autoPlayInterval / 100))
            } else {
                setCurrentFeature((prev) => (prev + 1) % features.length)
                setProgress(0)
            }
        }, 100)

        return () => clearInterval(timer)
    }, [progress, features.length, autoPlayInterval])

    return (
        <div className={cn("p-8 md:p-12 bg-white/5 rounded-[2rem] border border-white/5", className)}>
            <div className="max-w-7xl mx-auto w-full">
                <h2 className="text-3xl md:text-4xl lg:text-5xl font-display font-bold mb-10 text-center text-white">
                    {title}
                </h2>

                <div className="flex flex-col md:grid md:grid-cols-2 gap-6 md:gap-10">
                    <div className="order-2 md:order-1 space-y-8">
                        {features.map((feature, index) => (
                            <motion.div
                                key={index}
                                className="flex items-center gap-6 md:gap-8 cursor-pointer"
                                initial={{ opacity: 0.3 }}
                                animate={{ opacity: index === currentFeature ? 1 : 0.3 }}
                                transition={{ duration: 0.5 }}
                                onClick={() => {
                                    setCurrentFeature(index)
                                    setProgress(0)
                                }}
                            >
                                <motion.div
                                    className={cn(
                                        "w-8 h-8 md:w-10 md:h-10 rounded-full flex items-center justify-center border-2 transition-all duration-500",
                                        index === currentFeature
                                            ? "bg-purple-600 border-purple-500 text-white scale-110 shadow-[0_0_20px_rgba(147,51,234,0.3)]"
                                            : "bg-white/5 border-white/10 text-white/40",
                                    )}
                                >
                                    {index < currentFeature ? (
                                        <span className="text-lg font-bold text-green-400">âœ“</span>
                                    ) : (
                                        <span className="text-lg font-semibold">{index + 1}</span>
                                    )}
                                </motion.div>

                                <div className="flex-1">
                                    <h3 className={cn(
                                        "text-xl md:text-2xl font-bold transition-all duration-500",
                                        index === currentFeature ? "text-white" : "text-white/40"
                                    )}>
                                        {feature.title || feature.step}
                                    </h3>
                                    <p className={cn(
                                        "text-sm md:text-lg transition-all duration-500",
                                        index === currentFeature ? "text-white/60" : "text-white/20"
                                    )}>
                                        {feature.content}
                                    </p>
                                </div>
                            </motion.div>
                        ))}
                    </div>

                    <div
                        className={cn(
                            "order-1 md:order-2 relative overflow-hidden rounded-[2rem] border border-white/10 bg-black/40 shadow-2xl",
                            imageHeight
                        )}
                    >
                        <AnimatePresence mode="wait">
                            {features.map(
                                (feature, index) =>
                                    index === currentFeature && (
                                        <motion.div
                                            key={index}
                                            className="absolute inset-0 rounded-[2rem] overflow-hidden"
                                            initial={{ y: 100, opacity: 0, rotateX: -20, scale: 0.9 }}
                                            animate={{ y: 0, opacity: 1, rotateX: 0, scale: 1 }}
                                            exit={{ y: -100, opacity: 0, rotateX: 20, scale: 0.9 }}
                                            transition={{ duration: 0.6, ease: [0.23, 1, 0.32, 1] }}
                                        >
                                            <Image
                                                src={feature.image}
                                                alt={feature.step}
                                                className="w-full h-full object-cover transition-transform transform hover:scale-105 duration-700"
                                                width={1000}
                                                height={500}
                                                unoptimized={feature.image.startsWith('http')}
                                            />
                                            <div className="absolute inset-0 bg-gradient-to-t from-[#11141D] via-transparent to-transparent opacity-60" />

                                            {/* Progress bar */}
                                            <div className="absolute bottom-0 left-0 h-1.5 bg-purple-600 transition-all duration-100 ease-linear shadow-[0_0_10px_rgba(147,51,234,0.5)]"
                                                style={{ width: `${progress}%` }}
                                            />
                                        </motion.div>
                                    ),
                            )}
                        </AnimatePresence>
                    </div>
                </div>
            </div>
        </div>
    )
}
